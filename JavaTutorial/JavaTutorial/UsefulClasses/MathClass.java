import java.util.*;

public class MathClass {
    public static void main(String[] args) {
        // Math class methods is static = meaning it doesn't need to have an instance object

        // methods of the Math Class
        // Math.abs()
        System.out.println(Math.abs(-898098907)); // return the absoulute value of a number

        // Math.ceil()
        System.out.println(Math.ceil(7.4)); // return a rounded up value of float type

        // Math.floor()
        System.out.println(Math.floor(9.9)); // return a rounded down value of a float type

        // Math.max()
        System.out.println(Math.max(2, 5.6650)); // return the highest value from the 2 parameters

        // Math.min()
        System.out.println(Math.min(99.9, 45789.2356)); // return the lowest value from the 2 parameters

        // Math.pow()
        System.out.println(Math.pow(5, 8)); // return the power of the value

        // Math.sqrt()
        System.out.println(Math.sqrt(9)); // return the square root of an integer

    }
}
