class NamingVariable {
    public static void main (String[] args) {
        // valid variable name
        String name = "Frederick"; // can begin with letter
        int _sum = 5 + 5; // can begin with underscore ("_")
        int $tuition; // can begin with dollar sign ("$")
        public static final int CONSTANT_VARIABLE; // to create constant variable

        // naming variable notations:
        boolean gameOver = true; // camelCase (notation used by Java Programmers)
        int AllTimeHighScore; // CapitalLetter // class Name
        String first_name; // under_score
    }
}
